def gen(num, title, subtitle, slide_contents, next_t=""):
    """Generic section generator. slide_contents = list of (title, body_html, footer)"""
    slides = ""
    for i, (st, sb, sf) in enumerate(slide_contents):
        if sb.startswith("COL:"):
            slides += cnt_col(i+2, st, sb[4:], sf)
        else:
            slides += cnt(i+2, st, sb, sf)
    total = len(slide_contents) + 3  # cover + content + quiz + closing
    return wrap(num, title, subtitle, total, slides, next_t, QUIZZES.get(num))

def S(num, title, subtitle, slides_data):
    """Shorthand"""
    nt = NEXT_TITLES.get(num, "")
    return gen(num, title, subtitle, slides_data, nt)

